import mongoose from "mongoose";

// -------------- Product Collection Schema ----------------

const productSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    isAvailable: {
      type: Boolean,
      default: true,
    },
  },
  {
    timestamps: true,
  },
);

// -------------- Define Model ----------------

export const Product = mongoose.model(
  "Product",
  productSchema,
);
